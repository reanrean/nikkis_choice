function searchGenre(setName){
    if (setName == '') return '';
    for (var i = 0; i < setcategory.length; i++) {
        if (setcategory[i][1] == setName) return setcategory[i][0];
    }
    return '';
}

function checkPast(setName){
    if (setName == '') return '';
    for (var i = 0; i < modules_top.length; i++) {
        if (modules_top[i][0] != '往期上架' && modules_top[i][0].indexOf('往期')>=0 && modules_top[i][1] == setName)  {
            console.log(setName);
            break;
        }
    }
}

function presetModules(){
	//签到
	var latest = 1;
	for (var i = wardrobe.length - 1; i >= 0; i--){
		if (wardrobe[i][15].indexOf('签到·')==0) {
			latest = wardrobe[i][15].replace(/[^0-9]*/g,'');
			break;
		}
	}
	for (var i = 1; i <= latest; i++){
		modules_top.push(['签到',i+'月',0,'签到·'+i+'月','来源']);
	}

    var tmp_sets = ['黑天鹅']; //initialized: skip list
    var tmp_append = [];

    if (typeof setcategory == "undefined") setcategory = [];

	for (var i = 0; i < wardrobe.length; i++){
        //金钻套装成就
		if (wardrobe[i][18]=='套·金') tmp_append.push(['商店·金币', wardrobe[i][16].replace(/·套/g,''), 0, '!'+wardrobe[i][0], '名称']);
		else if (wardrobe[i][18]=='套·钻') tmp_append.push(['商店·钻石', wardrobe[i][16].replace(/·套/g,''), 0, '!'+wardrobe[i][0], '名称']);
        //图纸
        else if (wardrobe[i][15]=='设·图' && wardrobe[i][16]) {
            var setName = wardrobe[i][16];
            if ($.inArray(setName, tmp_sets) >= 0) continue;
            var genre = searchGenre(setName);
            if ($.inArray(genre, tz_genres) >= 0) {
                checkPast(setName);
                modules_top.push([genre+'图纸', setName , 1, setName]);
            }
            tmp_sets.push(setName);
        }
        //金套
        else if (wardrobe[i][15].indexOf('店·金币')>=0 && wardrobe[i][16]) {
            var setName = wardrobe[i][16].replace(/·套/g,'').replace(/·染/g,'').replace(/·基/g,'');
            if ($.inArray(setName, tmp_sets) < 0) {
                checkPast(setName);
                tmp_sets.push(setName);
            }
            tmp_append.push(['商店·金币', setName, 0, '!'+wardrobe[i][0], '名称']);
        }
        //钻套
        else if (wardrobe[i][15].indexOf('店·钻石')>=0 && wardrobe[i][16]) {
            var setName = wardrobe[i][16].replace(/·套/g,'').replace(/·染/g,'').replace(/·基/g,'');
            if ($.inArray(setName, tmp_sets) < 0) {
                checkPast(setName);
                tmp_sets.push(setName);
            }
            tmp_append.push(['商店·钻石', setName, 0, '!'+wardrobe[i][0], '名称']);
        }
        //map_src_module
        else if (wardrobe[i][15] in map_src_module) {
            if (wardrobe[i][16]) {
                var setName = wardrobe[i][16].replace(/·套/g,'').replace(/·染/g,'').replace(/·基/g,'');
                if ($.inArray(setName, tmp_sets) >= 0) continue;
                checkPast(setName);
                tmp_sets.push(setName);
                modules_top.push([map_src_module[wardrobe[i][15]], setName , 1, setName]);
            } else {
                var setName = wardrobe[i][15] + '*非套装';
                if ($.inArray(setName, tmp_sets) >= 0) continue;
                checkPast(setName);
                tmp_sets.push(setName);
                modules_top.push([map_src_module[wardrobe[i][15]], '散件', 0, setName, '来源']);
            }
        }
        //map_src_module_past
        else if (wardrobe[i][15] in map_src_module_past) {
            if (wardrobe[i][16]) {
                var setName = wardrobe[i][16].replace(/·套/g,'').replace(/·染/g,'').replace(/·基/g,'');
                if ($.inArray(setName, tmp_sets) >= 0) continue;
                tmp_sets.push(setName);
                modules_top.push([map_src_module_past[wardrobe[i][15]], setName , 1, setName]);
            } else {
                var setName = wardrobe[i][15] + '*非套装';
                if ($.inArray(setName, tmp_sets) >= 0) continue;
                tmp_sets.push(setName);
                modules_top.push([map_src_module_past[wardrobe[i][15]], '散件', 0, setName, '来源']);
            }
        }
	}
    modules_top = modules_top.concat(tmp_append);
}
